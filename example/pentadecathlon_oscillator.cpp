// Implements a pentadecathlon oscillator and steps 30 times
// pentadecathlon oscillators have a period of 15 

#include "../single_include/gol/gol.hpp"

#include <iostream>

gol::Board board(18, 11);

// Prints the board and then updates with next step
void printAndStep() {
    std::cout << "\n";
    for(int i = 0; i < board.height(); i++) {
        for(int j = 0; j < board.width(); j++) {
            std::cout << board[i][j] << " ";
        }
        std::cout << "\n";
    }
    board.nextStep(); // Update with next step with rule_string B3/S23
}

int main() {

    for(int i = 0; i < board.height(); i++) {
        for(int j = 0; j < board.width(); j++) {
            board[i][j] = false;
        }
    }

    // Make pentadecathlon oscillator
    board[3][4] = true;
    board[3][5] = true;
    board[3][6] = true;
    board[4][5] = true;
    board[5][5] = true;
    board[6][4] = true;
    board[6][5] = true;
    board[6][6] = true;
    board[8][4] = true;
    board[8][5] = true;
    board[8][6] = true;
    board[9][4] = true;
    board[9][5] = true;
    board[9][6] = true;
    board[11][4] = true;
    board[11][5] = true;
    board[11][6] = true;
    board[12][5] = true;
    board[13][5] = true;
    board[14][4] = true;
    board[14][5] = true;
    board[14][6] = true;

    for(int i = 0; i < 30; i++) {
        printAndStep();
    }

}

